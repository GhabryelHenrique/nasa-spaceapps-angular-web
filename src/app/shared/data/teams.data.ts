export interface TeamMember {
  user: string;
  userDetails: {
    id: string;
    fullName: string;
    username: string;
    country: string;
    avatar?: {
      id: string;
      title: string;
      alt: string;
      rendition: {
        url: string;
        fullUrl: string;
        height: number;
        width: number;
      };
    };
  };
}

export interface Challenge {
  id: string;
  title: string;
  excerpt: string;
}

export interface Location {
  id: string;
  title: string;
  displayName: string;
  country: string;
}

export interface Project {
  id: string;
  name: string;
  summary: string;
  demoLink: string;
  projectLink: string;
  details: string;
  solution: string;
  isSubmitted: boolean;
  submittedAt: string | null;
}

export interface Team {
  id: string;
  title: string;
  excerpt: string | null;
  description: string | null;
  desiredSkills: string[];
  languages: string[];
  owner: string;
  ownerDetails: {
    id: string;
    username: string;
  } | null;
  project: string;
  projectDetails: Project;
  projectSubmitted: boolean;
  featuredImage?: {
    id: string;
    title: string;
    alt: string;
    rendition: {
      url: string;
      fullUrl: string;
      height: number;
      width: number;
    };
  };
  challenge: string;
  challengeDetails: Challenge;
  location: string;
  locationDetails: Location;
  joinEnabled: boolean;
  memberships: TeamMember[] | null;
  nominationBadges: string[];
  awardBadges: string[] | null;
}

